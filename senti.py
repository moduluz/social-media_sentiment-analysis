column_names = ['target','id','date','flag','user','text']
twitter_data = pd.read_csv('/content/training.1600000.processed.noemoticon.csv', names=column_names,encoding = 'ISO-8859-1')

twitter_data.shape

twitter_data.head()

twitter_data.isnull().sum()

